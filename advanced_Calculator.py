num1 = float(input("Enter the First Number "))
OP =    (input("Enter Operator "))
num2 = float(input("Enter the Second Number "))

if OP == "+":
    print(num1 + num2)

elif OP == "-":
    print(num1 - num2)

elif OP == "*":
    print(num1 * num2)

elif OP == "/":
    print(num1 / num2)

else:
    print("Invalid....")